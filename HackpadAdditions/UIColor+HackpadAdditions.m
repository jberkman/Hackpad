#import "UIColor+HackpadAdditions.h"

@implementation UIColor (HackpadAdditions)

+ (UIColor *)hp_darkGreenColor
{
    return [UIColor colorWithRed:0x4B / 256.0
                           green:0x89 / 256.0
                            blue:0x71 / 256.0
                           alpha:1.0];
}

+ (UIColor *)hp_mediumGreenGrayColor
{
    return [UIColor colorWithRed:0xc3 / 256.0
                           green:0xcc / 256.0
                            blue:0xc9 / 256.0
                           alpha:1.0];
}

+ (UIColor *)hp_lightGreenGrayColor
{
    return [UIColor colorWithRed:0xe2 / 256.0
                           green:0xe6 / 256.0
                            blue:0xe4 / 256.0
                           alpha:1.0];
}

+ (UIColor *)hp_yellowColor
{
    return [UIColor colorWithRed:0xff / 256.0
                           green:0xf7 / 256.0
                            blue:0x9d / 256.0
                           alpha:1.0];
}

+ (UIColor *)hp_redColor
{
    return [UIColor colorWithRed:0xbe / 256.0
                           green:0x1e / 256.0
                            blue:0x2d / 256.0
                           alpha:1.0];
}

+ (UIColor *)hp_darkGrayColor
{
    return [UIColor colorWithRed:0x33 / 256.0
                           green:0x33 / 256.0
                            blue:0x35 / 256.0
                           alpha:1.0];
}

+ (UIColor *)hp_reallyDarkGrayColor
{
    return [UIColor colorWithRed:0x27 / 256.0
                           green:0x27 / 256.0
                            blue:0x29 / 256.0
                           alpha:1.0];
}

@end
